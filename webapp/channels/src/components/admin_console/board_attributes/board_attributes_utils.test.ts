import type {PropertyFieldOption} from '@mattermost/types/properties';

import {
    isCreatePending,
    isDeletePending,
    isOptionNameTaken,
    isPendingId,
    newPendingBoardField,
    newPendingId,
    stripPendingOptionIds,
} from './board_attributes_utils';

describe('board_attributes_utils', () => {
    describe('newPendingId / isPendingId', () => {
        test('newPendingId returns a string prefixed with pending_', () => {
            expect(newPendingId()).toMatch(/^pending_/);
        });

        test('newPendingId returns unique ids on each call', () => {
            const ids = new Set([newPendingId(), newPendingId(), newPendingId()]);
            expect(ids.size).toBe(3);
        });

        test('isPendingId is true for ids generated by newPendingId', () => {
            expect(isPendingId(newPendingId())).toBe(true);
        });

        test('isPendingId is false for non-pending ids', () => {
            expect(isPendingId('')).toBe(false);
            expect(isPendingId('real-server-id-abc')).toBe(false);
            expect(isPendingId('pending')).toBe(false);
            expect(isPendingId('pending-with-dash')).toBe(false);
        });
    });

    describe('stripPendingOptionIds', () => {
        test('returns undefined for undefined input', () => {
            expect(stripPendingOptionIds(undefined)).toBeUndefined();
        });

        test('returns empty array for empty input', () => {
            expect(stripPendingOptionIds([])).toEqual([]);
        });

        test('replaces pending ids with empty strings', () => {
            const pendingId = newPendingId();
            const options: PropertyFieldOption[] = [
                {id: pendingId, name: 'Pending option'},
            ];

            const result = stripPendingOptionIds(options);
            expect(result).toEqual([{id: '', name: 'Pending option'}]);
        });

        test('leaves real server ids untouched', () => {
            const options: PropertyFieldOption[] = [
                {id: 'real-server-id-abc123', name: 'Saved option', color: 'red'},
            ];

            const result = stripPendingOptionIds(options);
            expect(result).toEqual([{id: 'real-server-id-abc123', name: 'Saved option', color: 'red'}]);
        });

        test('handles mixed pending and real ids in one array', () => {
            const pendingId = newPendingId();
            const options: PropertyFieldOption[] = [
                {id: 'real-1', name: 'A'},
                {id: pendingId, name: 'B'},
                {id: 'real-2', name: 'C'},
            ];

            const result = stripPendingOptionIds(options);
            expect(result).toEqual([
                {id: 'real-1', name: 'A'},
                {id: '', name: 'B'},
                {id: 'real-2', name: 'C'},
            ]);
        });

        test('preserves option properties beyond id (name, color)', () => {
            const pendingId = newPendingId();
            const options: PropertyFieldOption[] = [
                {id: pendingId, name: 'X', color: 'blue'},
            ];

            const result = stripPendingOptionIds(options);
            expect(result?.[0]).toEqual({id: '', name: 'X', color: 'blue'});
        });
    });

    describe('newPendingBoardField', () => {
        test('returns a field with a pending id', () => {
            const field = newPendingBoardField({name: 'Priority'});
            expect(isPendingId(field.id)).toBe(true);
        });

        test('defaults to text type', () => {
            const field = newPendingBoardField({name: 'Priority'});
            expect(field.type).toBe('text');
        });

        test('zeroes out create_at/delete_at/update_at to mark as create-pending', () => {
            const field = newPendingBoardField({name: 'Priority'});
            expect(field.create_at).toBe(0);
            expect(field.delete_at).toBe(0);
            expect(field.update_at).toBe(0);
            expect(isCreatePending(field)).toBe(true);
        });

        test('sets group_id to "boards" and target_type to "system"', () => {
            const field = newPendingBoardField({name: 'Priority'});
            expect(field.group_id).toBe('boards');
            expect(field.target_type).toBe('system');
            expect(field.object_type).toBe('post');
        });

        test('assigns pending ids to each option in attrs.options', () => {
            const field = newPendingBoardField({
                name: 'Status',
                type: 'select',
                attrs: {
                    sort_order: 0,
                    options: [
                        {id: 'whatever', name: 'Todo'},
                        {id: '', name: 'Done'},
                    ],
                },
            });

            const options = field.attrs.options ?? [];
            expect(options).toHaveLength(2);
            expect(options.every((o) => isPendingId(o.id))).toBe(true);

            // ids must be unique across options so chips remain individually keyable
            const ids = new Set(options.map((o) => o.id));
            expect(ids.size).toBe(2);
        });

        test('does not touch attrs.options when not provided', () => {
            const field = newPendingBoardField({name: 'Title'});
            expect(field.attrs.options).toBeUndefined();
        });
    });

    describe('isCreatePending / isDeletePending', () => {
        test('isCreatePending true when create_at and delete_at are both 0', () => {
            expect(isCreatePending({id: 'x', create_at: 0, delete_at: 0})).toBe(true);
        });

        test('isCreatePending false when create_at is non-zero (already saved)', () => {
            expect(isCreatePending({id: 'x', create_at: 1234, delete_at: 0})).toBe(false);
        });

        test('isDeletePending true when item was saved (create_at > 0) and is now flagged for delete (delete_at > 0)', () => {
            expect(isDeletePending({create_at: 1234, delete_at: 5678})).toBe(true);
        });

        test('isDeletePending false for never-saved items, even if delete_at is set', () => {
            // An item that was added and removed before saving is neither create-pending nor delete-pending
            expect(isDeletePending({create_at: 0, delete_at: 5678})).toBe(false);
        });
    });

    describe('isOptionNameTaken', () => {
        const options: PropertyFieldOption[] = [
            {id: '1', name: 'Todo'},
            {id: '2', name: 'In Progress'},
            {id: '3', name: 'Done'},
        ];

        test('returns true when candidate matches another option (case-insensitive)', () => {
            expect(isOptionNameTaken('todo', options)).toBe(true);
            expect(isOptionNameTaken('TODO', options)).toBe(true);
            expect(isOptionNameTaken('Todo', options)).toBe(true);
        });

        test('returns true when candidate has whitespace padding matching another option', () => {
            expect(isOptionNameTaken('  Todo  ', options)).toBe(true);
        });

        test('returns false when candidate is unique', () => {
            expect(isOptionNameTaken('Blocked', options)).toBe(false);
        });

        test('excludes a given option from the conflict check (used during rename)', () => {
            // Renaming "Todo" → "Todo" should not flag itself as a conflict
            expect(isOptionNameTaken('Todo', options, options[0])).toBe(false);
        });

        test('still flags conflicts against other options when excluding', () => {
            // Renaming option 1 to "Done" should still conflict with option 3
            expect(isOptionNameTaken('Done', options, options[0])).toBe(true);
        });
    });
});
