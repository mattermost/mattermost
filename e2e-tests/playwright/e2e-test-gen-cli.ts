#!/usr/bin/env node
/**
 * E2E Test Generation CLI
 *
 * Advanced runner for Playwright test generation with MCP support.
 * This file enables the e2e-agents package to trigger the full generation pipeline.
 *
 * Usage:
 *   npx tsx e2e-test-gen-cli.ts generate "Flow Name" --output ./specs/flow-dir
 *   npx tsx e2e-test-gen-cli.ts heal ./specs/flow-dir
 */

import { execFileSync } from 'child_process';
import * as fs from 'fs';
import * as path from 'path';

const args = process.argv.slice(2);
const command = args[0];

async function main() {
  if (!command || (command !== 'generate' && command !== 'heal')) {
    console.error('Usage: e2e-test-gen-cli.ts <generate|heal> [args...]');
    process.exit(1);
  }

  if (command === 'generate') {
    // Flow name is required
    const flowName = args[1];
    if (!flowName) {
      console.error('Usage: e2e-test-gen-cli.ts generate "<Flow Name>" --output <dir> [options]');
      process.exit(1);
    }

    // Parse output directory
    let outputDir = 'specs/functional/ai-assisted/flow';
    const outputIdx = args.indexOf('--output');
    if (outputIdx !== -1 && outputIdx + 1 < args.length) {
      outputDir = args[outputIdx + 1];
    }

    // Parse other options
    let scenarios = 3;
    let baseUrl = 'http://localhost:8065';
    let headless = false;
    let browser = 'chrome';
    let project = 'chrome';
    let parallel = false;
    let useMcp = false;

    const scenarioIdx = args.indexOf('--scenarios');
    if (scenarioIdx !== -1 && scenarioIdx + 1 < args.length) {
      scenarios = parseInt(args[scenarioIdx + 1], 10) || 3;
    }

    const baseUrlIdx = args.indexOf('--base-url');
    if (baseUrlIdx !== -1 && baseUrlIdx + 1 < args.length) {
      baseUrl = args[baseUrlIdx + 1];
    }

    if (args.includes('--headless')) {
      headless = true;
    }

    const browserIdx = args.indexOf('--browser');
    if (browserIdx !== -1 && browserIdx + 1 < args.length) {
      browser = args[browserIdx + 1];
    }

    const projectIdx = args.indexOf('--project');
    if (projectIdx !== -1 && projectIdx + 1 < args.length) {
      project = args[projectIdx + 1];
    }

    if (args.includes('--parallel')) {
      parallel = true;
    }

    if (args.includes('--mcp')) {
      useMcp = true;
    }

    // Create output directory
    fs.mkdirSync(outputDir, { recursive: true });

    // Generate a basic test file
    const slug = flowName
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, '_')
      .replace(/^_+|_+$/g, '');

    const testFile = path.join(outputDir, `${slug}.spec.ts`);

    const testContent = generateTestTemplate(flowName, baseUrl, headless);

    fs.writeFileSync(testFile, testContent, 'utf-8');

    console.log(`Generated: ${testFile}`);
    process.exit(0);
  }

  if (command === 'heal') {
    const specDir = args[1];
    if (!specDir || !fs.existsSync(specDir)) {
      console.error(`Spec directory not found: ${specDir}`);
      process.exit(1);
    }

    // Parse options
    const browser = args.includes('--browser') ? args[args.indexOf('--browser') + 1] : 'chrome';
    const project = args.includes('--project') ? args[args.indexOf('--project') + 1] : 'chrome';
    const parallel = args.includes('--parallel');
    const useMcp = args.includes('--mcp');

    // Run playwright test on the spec directory using execFileSync
    try {
      const playwrightArgs = [
        'test',
        specDir,
        `--project=${project}`,
      ];

      if (!parallel) {
        playwrightArgs.push('--workers=1');
      }

      console.log(`Running: npx ${playwrightArgs.join(' ')}`);
      execFileSync('npx', playwrightArgs, {
        stdio: 'inherit',
        cwd: process.cwd(),
      });

      console.log('✅ Heal validation passed');
      process.exit(0);
    } catch (error) {
      console.log('⚠️  Heal validation failed');
      process.exit(1);
    }
  }
}

